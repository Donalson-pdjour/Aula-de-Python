# Verifica se um número é divisível por 13 e 26 (ou seja, por 26)
n = int(input("Digite um número: "))

if n % 26 == 0:
    print(f"{n} é divisível por 13 e 26.")
else:
    print(f"{n} NÃO é divisível por 13 e 26.")
    
